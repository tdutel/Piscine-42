int	ft_str_is_alpha(char *str)
{
	int	i;
	int	a;
	int	b;

	i = 0;
	a = 0;
	b = 1;
	while (str[i])
	{
		if ((str[i] > '@' && str[i] < '[') || (str[i] >= 'a' && str[i] <= 'z'))
			a = 1;
		else
			b = 0;
		i++;
	}
	return (b);
}
