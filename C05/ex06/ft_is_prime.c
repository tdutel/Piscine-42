int	ft_is_prime(int nb)
{
	int	i;
	int	nbr;

	i = 2;
	if (nb == 1 || nb <= 0)
		return (0);
	while (i < nb)
	{
		nbr = nb % i;
		if (nbr == 0)
			return (0);
		i++;
	}
	return (1);
}
